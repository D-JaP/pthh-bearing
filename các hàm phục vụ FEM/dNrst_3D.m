            [-(1/8 - 1/8*S)*(1 + T), -(1/8 - 1/8*R)*(1 + T), 1/8*(1 - R)*(1 - S)];...
            [1/8*(1 - S)*(1 + T), -(1/8 + 1/8*R)*(1 + T), 1/8*(1 + R)*(1 - S)];...
